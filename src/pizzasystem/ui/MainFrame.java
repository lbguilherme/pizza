    private void GoBack() {
        switch(this.LoginPower) {
            case 1: // admin
                MainJPanel.removeAll();
                MainJPanel.repaint();
                MainJPanel.revalidate();

                MainJPanel.add(Admin);
                MainJPanel.repaint();
                MainJPanel.revalidate();
                break;
            case 2: // atendente
                MainJPanel.removeAll();
                MainJPanel.repaint();
                MainJPanel.revalidate();

                MainJPanel.add(Atendente);
                MainJPanel.repaint();
                MainJPanel.revalidate();
                break;
            case 3: // pizzaiolo
                MainJPanel.removeAll();
                MainJPanel.repaint();
                MainJPanel.revalidate();

                MainJPanel.add(Pizzaiolo);
                MainJPanel.repaint();
                MainJPanel.revalidate();
                break;
            case 4: // entregador
                MainJPanel.removeAll();
                MainJPanel.repaint();
                MainJPanel.revalidate();

                MainJPanel.add(Entregador);
                MainJPanel.repaint();
                MainJPanel.revalidate();
                break;
            default: // outros(teste/error)
                this.Logout();
                break;
                
        }
    }
